use super::{h, ht};

#[test]
fn ht_matches_known_vector() {
    let digest = ht("veen/profile", b"hello world");
    let expected = [
        0x01, 0xa5, 0x69, 0xeb, 0xb6, 0x9d, 0xdd, 0x4d, 0x66, 0xe6, 0x48, 0xa0, 0xfb, 0xe6, 0x24,
        0x7a, 0x36, 0xb2, 0x7f, 0x1d, 0x08, 0xe1, 0x8d, 0x08, 0xff, 0x7b, 0x3b, 0xac, 0x14, 0x86,
        0x94, 0x83,
    ];
    assert_eq!(digest, expected);
}

#[test]
fn h_matches_known_vector() {
    let digest = h(b"wallet.open.v1");
    let expected = [
        0xf1, 0xb3, 0x7e, 0x20, 0x7e, 0x51, 0xe7, 0x8a, 0x9f, 0x83, 0xb0, 0x76, 0xad, 0x5d, 0x14,
        0xed, 0xf9, 0xb8, 0x11, 0xa9, 0x80, 0x5d, 0x2d, 0xc4, 0x03, 0xe2, 0x08, 0x2f, 0x9c, 0x92,
        0x4a, 0xea,
    ];
    assert_eq!(digest, expected);
}
